char	*ft_strcat(char *s1, const char *s2)
{
	char	*res;
	int		i;
	int		j;

	res = (char*)s1;
	i = 0;
	j = 0;
	while (s1[i])
		i++;
	while (s2[j])
	{
		res[i] = s2[j];
		i++;
		j++;
	}
	res[i] = '\0';
	return (res);
}
